// تابع تبدیل متن به HTML
function textToHTML(text) {
    const lines = text.trim().split('\n');
    let html = '<div class="accordion">';
    let currentMainSection = '';
    let currentSubSection = '';
    let subSectionContent = '';

    lines.forEach(line => {
        line = line.trim();

        // شناسایی عنوان اصلی (بدون نشانه‌گذاری مثل • یا -)
        if (!line.startsWith('•') && !line.startsWith('-') && line && !currentSubSection) {
            if (currentMainSection) {
                html += '</div></div>'; // بستن accordion-item قبلی
            }
            currentMainSection = line;
            html += `
                <div class="accordion-item">
                    <button class="accordion-header">${currentMainSection}</button>
                    <div class="accordion-content">
            `;
        }
        // توضیحات اصلی زیر عنوان اصلی
        else if (!line.startsWith('•') && !line.startsWith('-') && currentMainSection && !currentSubSection) {
            html += `<p>${line}</p><ul>`;
        }
        // شناسایی زیرعنوان (با • شروع می‌شود)
        else if (line.startsWith('•')) {
            if (currentSubSection) {
                html += `${subSectionContent}</ul></li>`; // بستن زیرعنوان قبلی
            }
            currentSubSection = line.replace('•', '').trim();
            subSectionContent = `<li><strong>${currentSubSection}</strong>`;
        }
        // شناسایی شروط یا اثرات (با - شروع می‌شود)
        else if (line.startsWith('-')) {
            const content = line.replace('-', '').trim();
            if (content.startsWith('شرط:')) {
                subSectionContent += `<ul><li><em>شرط:</em> ${content.replace('شرط:', '').trim()}</li>`;
            } else {
                subSectionContent += `<li>${content}</li>`;
            }
        }
    });

    // بستن آخرین زیرعنوان و بخش اصلی
    if (currentSubSection) {
        html += `${subSectionContent}</ul></li>`;
    }
    html += `</ul></div></div></div>`;

    return html;
}

